public class OneToTen
{
    public static void main(String [] args)
    {
        int i = 1;
        // Use a loop to print out the numbers
        while (i < 11)
            {
            System.out.print(i + " ");
            i = i + 1;
            }
       
        System.out.println(); // Finish with a new line.
    }
}
